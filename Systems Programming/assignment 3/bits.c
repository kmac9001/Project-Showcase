/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int ascii_min = 48;
  int ascii_max = 57;
  int neg_x = x >> 31;
  neg_x &= 1;
  int less = x + (~ascii_min + 1);
  less >>= 31;
  less &= 1;
  int more = x + (~ascii_max);
  more >>= 31;
  more &= 1;
  return !neg_x & !less & !!more;
}

