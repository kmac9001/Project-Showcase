/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int neg_x = x >> 31;
  int neg_y = y >> 31;

  int neg = neg_x ^ neg_y;
  int pos = !neg_x & !neg_y;

  pos <<= 31;

  int max = ~0 & ~pos;
  
  int min = (neg_x & neg_y) & (!((x ^ 1 << 31) & (y ^ 1 << 31)));

  max += (~x + 1);
  max += (~y + 1);
   
  return !!neg | !(max >> 31) & !!(max+1) & !min | !x | !y;
}

